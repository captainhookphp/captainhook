 * @link    https://github.com/captainhookphp/captainhook
 * @since   Class available since Release 5.19.0
 */
interface Action
{
    /**
     * Executes the action
     *
     * @param  \CaptainHook\App\Config           $config
     * @param  \CaptainHook\App\Console\IO       $io
